let x = '10';
if (x == 1) {
    x += 1;
} else if (x == 5) {
    x += 2;
} else if (x == 10) { //=>'10'==10  把字符串转换为数字  条件成立
    x += 3;
    // x = x + 3  => '103'
} else {
    x += 4;
}
console.log(x);
