let x = 10;
if (x) {
    // 条件（验证真假的过程）：
    // x==10 x===10  x>10  x<10  x>=10 ...
    // x 把当前值转换为布尔类型验证真假
    // x>=0 && x<=10  或者  x==1 || x==2
    // ....
    x++;
} else {
    // 以上条件都不成立，执行else
    x--;
}
console.log(x);
